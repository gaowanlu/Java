package 基本数据类型;
public class BaseType {
    public static void main(String[]args){
        byte var_byte=1;//1个字节
        short var_short=2;//2个字节
        int var_int=123;//4个字节
        long var_long=89;//8个字节
        float var_float=89.23F;//4个字节
        double var_double=23.43D;//8个字节
        char var_char='s';//2个字节
        char var_unicode='\u0000';//4个字节
        System.out.println(var_byte);
        System.out.println(var_short);
        System.out.println(var_int);
        System.out.println(var_long);
        System.out.println(var_float);
        System.out.println(var_double);
        System.out.println(var_char);
        System.out.println(var_unicode);
    }
}
